const getFolderIcon = () => {
    return getIcon(folderIcon, 'folderIcon');
}

const getFileIcon = () => {
    return getIcon(fileIcon, 'fileIcon');
}

const getIcon = (icon: Array<number[]>, iconCanvasName: string) => {
    setupWindowBackgrounds();
    const iconPromise = new Promise<string>((resolve) => {
        if (typeof window !== 'undefined') {
            if (window.icons.folder && window.icons.folder !== '') {
                resolve(window.icons.folder);
            } else {
                window.setTimeout(() => {
                    const canvas = document.getElementById(iconCanvasName) as HTMLCanvasElement;
                    // I control the vertical, and the horizontal!
                    // therefore force non-null cuz I know I added that element :-D
                    const context = canvas!.getContext('2d')!;
                    context.strokeStyle = 'rgba(0,0,0,1)';
                    const maxY = icon.length;
                    const maxX = icon[0].length;
                    
                    for(let x = 0; x < maxX; x++) {
                        for (let y = 0; y < maxY; y++) {
                            if (icon[y][x] === 1) {
                                context.fillRect(x, y, 1, 1);
                            }
                        }
                    }

                    const img = canvas.toDataURL('image/gif');
                    window.icons.folder = img;
                    resolve(img);
                }, 0);
            }
        }
    });
    return iconPromise;
}

